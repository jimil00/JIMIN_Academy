
public class Exam_03_Variable {
	public static void main(String[] args) {
		
		String playername = "";
		//나는 이 공백을 playername이라는 변수명을 가진 변수에 넣어줄거야
		System.out.println(playername);
		
		playername = "강지민";
		//나는 강지민을 playername이라는 변수명을 가진 변수에 넣어줄거야
		System.out.println(playername);
		
		playername = "한설빈";
		//나는 한설빈을 playername이라는 변수명을 가진 변수에 넣어줄거야
		System.out.println(playername);
	}

}
