
public class Exam_02_While_01 {
	public static void main(String[] args) {
		
	int i = 0;
	//초기값 지정
	
	while(i<10) {
	//조건문. 괄호 안에 조건이 true면 중괄호 안의 문장들이 실행된다.
	//false면 실행되지 않고 끝난다.
		
		//i는 0부터고, i가 10보다 작을 동안에 이 문장을 실행해라.
		
		i++;
		//증감문. ++은 +1이다. i=i+1이라는 뜻.
		System.out.println("i = " + i);
	   }

	
		
	}

}
